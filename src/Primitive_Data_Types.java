public class Primitive_Data_Types {
       public static void main(String [] args){
           byte b1=10;
           byte b2 = 100;

           short s1 = 5;
           short s2 = -133;
           short s3 = 0;

           long l1 = 1000000000000000L;

           float f1 = 3.14F;
           float f2 = -2.5514f;
           float f3 = 20.0f;

           double d1 = 7.59384303;
           double d2 = -1356.4387242;

           char c1 = 'a';
           char c2 = 'A';
           char c3 = '3';
           char c5 = 130;
           char c7 = '\u0030';

           System.out.println(c2);

           boolean bool1 = true;
           boolean bool2 = false;

           int a1 = 60;

           int a2 = 0b111100;
           int a3 = 0X3C;

           int a4 = 1_0_0_0_0_______00;

           System.out.println(a4);
       }
}
